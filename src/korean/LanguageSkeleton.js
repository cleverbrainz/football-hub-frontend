function convert(arr) {
  let obj = {}

  arr.forEach(x => {
    if (!(x.page in obj)) {
      obj[x.page] = {
        [x.section]: {
          en: x.english,
          ko: x.korean
        }
      }
    } else {
      obj[x.page] = {
        ...obj[x.page],
        [x.section]: {
          en: x.english,
          ko: x.korean
        }
      }
    }
  })

  return obj
}

